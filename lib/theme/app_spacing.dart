class AppSpacing {
  const AppSpacing._();

  static const double _scaleRatio = 1.3;

  static const double sm = 8.0;

  static const double md = sm * _scaleRatio;
  static const double lg = md * _scaleRatio;
  static const double xl = lg * _scaleRatio;
  static const double xxl = xl * _scaleRatio;
  static const double xxxl = xxl * _scaleRatio;

  static const double xs = sm / _scaleRatio;
  static const double xxs = xs / _scaleRatio;
  static const double xxxs = xxs / _scaleRatio;
}
